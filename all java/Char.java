class Char {
    public static void main(String[] args) {
        char grade = 'B';
        System.out.println("Grade: " + grade);

        char section = 'C';
        System.out.println("Section: " + section);

        char initial = 'K';
        System.out.println("Initial: " + initial);

        char currency = 's';
        System.out.println("Currency: " + currency);

        char percent = '%';
        System.out.println("Percent: " + percent);

        char ampersand = '&';
        System.out.println("Ampersand: " + ampersand);

        char questionMark = '?';
        System.out.println("Question Mark: " + questionMark);

        char exclamationMark = '!';
        System.out.println("Exclamation Mark: " + exclamationMark);

        char atSymbol = '#';
        System.out.println("At Symbol: " + atSymbol);

        char hashSymbol = '@';
        System.out.println("Hash Symbol: " + hashSymbol);
    }
}
